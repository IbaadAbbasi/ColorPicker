import androidx.compose.ui.graphics.Color
import androidx.compose.ui.graphics.toArgb

fun hsvToColor(hue: Float, saturation: Float, brightness: Float, alpha: Float): Color {
    val hsv = floatArrayOf(hue, saturation, brightness)
    val argb = android.graphics.Color.HSVToColor((alpha * 255).toInt(), hsv)
    return Color(argb)
}

fun colorToHex(color: Color): String =
    String.format("#%08X", color.toArgb()).uppercase()


